"""
Create folder/package directory scaffolds inside the sandbox only.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

from workflow_dataset.materialize.materialize_models import MaterializedArtifact
from workflow_dataset.materialize.workspace_manager import ensure_workspace_dir
from workflow_dataset.utils.dates import utc_now_iso
from workflow_dataset.utils.hashes import stable_id

# Standard scaffold folder names (no user paths)
COMMON_SCAFFOLD_DIRS = [
    "briefs",
    "references",
    "exports",
    "revisions",
    "assets",
    "reporting",
    "deliverables",
    "sheets",
    "notes",
    "docs",
    "source",
    "reviews",
]


def build_folder_scaffold(
    workspace_path: Path | str,
    folder_names: list[str],
    request_id: str = "",
    project_id: str = "",
    draft_ref: str = "",
    include_readme: bool = True,
) -> MaterializedArtifact:
    """
    Create a set of empty directories and optionally a README in the workspace.
    Returns a single artifact record describing the scaffold.
    """
    workspace_path = Path(workspace_path)
    created: list[str] = []
    for name in folder_names:
        safe = name.replace("..", "").strip("/").replace("/", "_").replace(" ", "_")[:64]
        if not safe:
            continue
        d = ensure_workspace_dir(workspace_path, safe)
        try:
            rel = d.relative_to(workspace_path)
            created.append(str(rel))
        except ValueError:
            created.append(safe)
    if include_readme:
        readme = workspace_path / "README_scaffold.txt"
        readme.write_text(
            "Scaffold generated by materialization. Folders: " + ", ".join(created) + "\n",
            encoding="utf-8",
        )
        created.append("README_scaffold.txt")
    artifact_id = stable_id("art", "folder_scaffold", request_id or "scaffold", utc_now_iso(), prefix="art")
    return MaterializedArtifact(
        artifact_id=artifact_id,
        request_id=request_id,
        project_id=project_id,
        artifact_type="folder_scaffold",
        sandbox_path="",
        title="Folder scaffold",
        summary=f"Created: {', '.join(created)}",
        provenance_refs=[draft_ref] if draft_ref else [],
        created_utc=utc_now_iso(),
    )


def build_project_scaffold(
    workspace_path: Path | str,
    project_name: str,
    request_id: str = "",
    project_id: str = "",
) -> MaterializedArtifact:
    """Create a minimal project root scaffold (briefs, notes, docs)."""
    dirs = ["briefs", "notes", "docs"]
    return build_folder_scaffold(
        workspace_path,
        dirs,
        request_id=request_id,
        project_id=project_id,
        include_readme=True,
    )
